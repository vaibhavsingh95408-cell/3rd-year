    #include<iostream>
    using namespace std;
    int main()
    {
        for(int k=5;k>=1;k--)
        {
            for(int l=k;l<=5;l++)
            {
                cout<<"*";
            }
            cout<<endl;
        }
        
        
        
        
        for (int i=1;i<5;i++)
        {
            for(int j=i;j<5;j++)
            {
                cout<<"*";
            }
            cout<<endl;
        }
    }